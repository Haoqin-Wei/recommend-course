# ── Internal helpers ─────────────────────────────────────

def _best_section(sections: list[dict]) -> Optional[dict]:
    """Pick the section with the best professor rating."""
    best = None
    best_score = -1
    for sec in sections:
        pr = sec.get("professor_rating") or {}
        score = pr.get("overall") or 0
        if score > best_score:
            best_score = score
            best = sec
    return best


def _build_reasons(item: dict, section: Optional[dict], state: dict) -> str:
    """Build 1–3 sentence reason string."""
    reasons = []
    c = item["course"]
    grades = item.get("grade_distribution") or {}

    # Major match
    major = state.get("major")
    if major and major in c.get("major_requirement", []):
        reasons.append(f"Counts toward your {major} requirements.")

    # Grade ease
    avg_gpa = grades.get("avg_gpa", 0)
    if avg_gpa >= 3.3:
        reasons.append(f"Historically generous grading (avg GPA {avg_gpa:.1f}).")
    elif avg_gpa <= 2.7:
        reasons.append(f"Tends to be tough grading-wise (avg GPA {avg_gpa:.1f}).")

    # Professor
    if section:
        pr = section.get("professor_rating") or {}
        if pr.get("overall") and pr["overall"] >= 4.5:
            reasons.append(f"{section['instructor']} is highly rated ({pr['overall']}/5).")

    return " ".join(reasons) if reasons else "Solid option based on available data."


def _build_warnings(all_items: list[dict]) -> str:
    """Aggregate warnings from all candidate items."""
    warnings = []
    for item in all_items:
        cid = item["course"]["course_id"]
        if not item["prereq_met"]:
            warnings.append(f"- {cid} requires {', '.join(item['prereq_missing'])} which you haven't completed yet.")
        if item["has_conflict"]:
            warnings.append(f"- {cid} has a time conflict with your current schedule.")
    return "\n".join(warnings)


def _empty_results_response(state: dict) -> str:
    """
    Maps to SKILL.md: 'Result Empty Handling.'
    Never just say 'no results.' Suggest loosening constraints.
    """
    suggestions = []
    if state.get("preferred_time"):
        suggestions.append("- Try widening your preferred time window")
    if state.get("difficulty_preference") == "easy":
        suggestions.append("- Consider including moderate-difficulty courses")
    if state.get("recommendation_goal") == "major_requirement":
        suggestions.append("- Look at electives or GE courses outside your major")

    suggestion_text = "\n".join(suggestions) if suggestions else "- Try broadening your search criteria"

    return (
        "I couldn't find courses that match all your criteria for this term. "
        "Here are some things you could try:\n"
        f"{suggestion_text}\n\n"
        "Want me to search again with looser filters?"
    )
